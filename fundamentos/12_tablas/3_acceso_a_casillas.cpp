/*
Acceso a las Casillas 
Para acceder a una casilla de una tabla se utiliza el nombre de la tabla i después entre corchetes el índice de la casilla que queremos utilizar. Cada casilla es una variable así que trataremos las casillas como si fueran variables con un nombre raro. Esto nos permite hacer cualquier cosa que sepamos hacer con variables con las casillas de una tabla.

Por ejemplo, las siguientes instrucciones declaran una tabla de 2 caracteres y la llenan con 2 valores:
 */

#include <iostream>
using namespace std;
int main(){
  char grupo[2];
grupo[0] = 'U';
grupo[1] = '2';
}